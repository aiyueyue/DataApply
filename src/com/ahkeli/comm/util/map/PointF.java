package com.ahkeli.comm.util.map;


public class PointF {
	private float x;
	private float y;
	
	public PointF(float x, float y) {
		this.x = x;
		this.y = y;
	}
	
	public PointF() {
	}

	public float getX() {
		return x;
	}
	public void setX(float x) {
		this.x = x;
	}
	public float getY() {
		return y;
	}
	public void setY(float y) {
		this.y = y;
	}
}
